// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•

async function main() {
  console.log("Starting career hub seed...");
  console.log(`Seeding ${internships.length} internships and ${jobs.length + governmentJobs.length} jobs...`);

  try {
    // Seed internships
    let internshipCount = 0;
    let internshipSkipped = 0;
    for (const internship of internships) {
      try {
        await prisma.internship.create({ data: internship });
        internshipCount++;
      } catch (e: any) {
        if (e.code === "P2002") {
          internshipSkipped++;
        } else {
          console.error(`Error creating internship "${internship.title}" at ${internship.company}:`, e.message);
        }
      }
    }
    console.log(`Internships: ${internshipCount} created, ${internshipSkipped} skipped (duplicates)`);

    // Seed jobs
    let jobCount = 0;
    let jobSkipped = 0;
    for (const job of [...jobs, ...governmentJobs]) {
      try {
        await prisma.jobListing.create({ data: job });
        jobCount++;
      } catch (e: any) {
        if (e.code === "P2002") {
          jobSkipped++;
        } else {
          console.error(`Error creating job "${job.title}" at ${job.company}:`, e.message);
        }
      }
    }
    console.log(`Jobs: ${jobCount} created, ${jobSkipped} skipped (duplicates)`);

    // Final counts
    const totalInternships = await prisma.internship.count();
    const totalJobs = await prisma.jobListing.count();
    console.log(`\nSeed complete!`);
    console.log(`Total internships in database: ${totalInternships}`);
    console.log(`Total jobs in database: ${totalJobs}`);
  } catch (error) {
    console.error("Seed failed:", error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

main();
